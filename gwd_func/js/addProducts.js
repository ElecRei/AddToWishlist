// Add and Remove Function

    // Product 01
            
    var selectedProduct1 = document.getElementById('product_01');

    var addedProduct1 = document.getElementById('wishlist_01');

    function showProduct1() {

        if(selectedProduct1.onclick){
            addedProduct1.style.display = 'block';
            //console.log('It worked!');   
        }

        else{
            console.log('Nothing happened');   
        }
    }

    function removeProduct1() {

        if(addedProduct1.style.display = 'block'){  
            addedProduct1.style.display = 'none';
        }

    }

